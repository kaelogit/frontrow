/**
 * Generates supabase/seed/sofi-qf-listings.sql + mock TypeScript
 * Run: node scripts/generate-sofi-qf-seed.mjs
 */

import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { SOFI_QF_LISTINGS, SOFI_SECTIONS } from "./data/sofi-qf-listings.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));

function frontrowlyPrice(marketNow) {
  return Math.round(marketNow * 90) / 100;
}

function sqlStr(s) {
  return s == null ? "NULL" : `'${String(s).replace(/'/g, "''")}'`;
}

function sqlArr(arr) {
  if (!arr?.length) return "ARRAY[]::TEXT[]";
  return `ARRAY[${arr.map((x) => sqlStr(x)).join(", ")}]`;
}

const minMarket = Math.min(...SOFI_QF_LISTINGS.map((l) => l[5]));
const minPrice = frontrowlyPrice(minMarket);

const listingRows = SOFI_QF_LISTINGS.map((l, i) => {
  const [section, product, row, qty, qtyAvail, marketNow, perks, badges, viewScore, viewLabel, type] = l;
  const price = frontrowlyPrice(marketNow);
  return `(${sqlStr(section)}, ${sqlStr(row)}, ${sqlStr(product)}, ${sqlStr(type)}, ${qty}, ${qtyAvail}, ${price.toFixed(2)}::DECIMAL, ${marketNow.toFixed(2)}::DECIMAL, 'USD', ${sqlArr(perks)}, ${sqlArr(badges)}, ${viewScore ?? "NULL"}, ${sqlStr(viewLabel)}, ${i})`;
}).join(",\n");

const sectionsSql = SOFI_SECTIONS.map(
  (s) => `  ('${s.num}', '${s.level}', '${s.zone}')`
).join(",\n");

const sql = `-- World Cup QF @ SoFi Stadium — Match 98
-- ${SOFI_QF_LISTINGS.length} ticket listings · Frontrowly prices = 10% below reference market
-- Safe to re-run (clears prior QF listings, upserts event + venue map)
-- Requires: world-cup-2026-full.sql (event world-cup-qf-match-98)

ALTER TABLE venues ADD COLUMN IF NOT EXISTS stadium_map_slug TEXT;
ALTER TABLE events ADD COLUMN IF NOT EXISTS seat_map_enabled BOOLEAN DEFAULT false;
ALTER TABLE events ADD COLUMN IF NOT EXISTS scarcity_override INTEGER;
ALTER TABLE events ADD COLUMN IF NOT EXISTS queue_enabled BOOLEAN DEFAULT false;
ALTER TABLE ticket_listings ADD COLUMN IF NOT EXISTS compare_at_price DECIMAL(12, 2);

UPDATE venues SET stadium_map_slug = 'sofi' WHERE slug = 'sofi-stadium';

UPDATE events SET
  seat_map_enabled = true,
  scarcity_override = 3,
  min_price = ${minPrice.toFixed(2)},
  image_url = '/images/category-world-cup.jpg'
WHERE slug = 'world-cup-qf-match-98';

INSERT INTO stadium_maps (venue_id, slug, name)
SELECT v.id, 'sofi', 'SoFi Stadium'
FROM venues v WHERE v.slug = 'sofi-stadium'
ON CONFLICT (venue_id, slug) DO NOTHING;

INSERT INTO stadium_sections (venue_id, section_number, level, zone)
SELECT v.id, s.num, s.lvl, s.zone
FROM venues v
CROSS JOIN (VALUES
${sectionsSql}
) AS s(num, lvl, zone)
WHERE v.slug = 'sofi-stadium'
ON CONFLICT (venue_id, section_number) DO NOTHING;

DELETE FROM ticket_listings
WHERE event_id = (SELECT id FROM events WHERE slug = 'world-cup-qf-match-98');

INSERT INTO ticket_listings (
  event_id, section_number, row_label, product_name, listing_type,
  quantity, quantity_available, price, compare_at_price,
  currency, perks, badges, view_score, view_label, sort_order
)
SELECT
  e.id,
  v.section_number,
  v.row_label,
  v.product_name,
  v.listing_type,
  v.quantity,
  v.quantity_available,
  v.price,
  v.compare_at_price,
  v.currency,
  v.perks,
  v.badges,
  v.view_score,
  v.view_label,
  v.sort_order
FROM events e
CROSS JOIN (VALUES
${listingRows}
) AS v(section_number, row_label, product_name, listing_type, quantity, quantity_available, price, compare_at_price, currency, perks, badges, view_score, view_label, sort_order)
WHERE e.slug = 'world-cup-qf-match-98';
`;

const mockRows = SOFI_QF_LISTINGS.map((row) => JSON.stringify(row)).join(",\n  ");

const mockTs = `/** @generated by scripts/generate-sofi-qf-seed.mjs — do not edit */
import type { TicketListing } from "@/types/database";
import { frontrowlyCompareAt, frontrowlyPrice } from "@/lib/pricing/frontrowly-price";

const EVENT_ID = "wc-98";

type Row = [
  string | null,
  string | null,
  string | null,
  number,
  number,
  number,
  string[],
  string[],
  number | null,
  string | null,
  TicketListing["listing_type"],
];

const RAW: Row[] = [
  ${mockRows}
];

function toListing(id: string, row: Row): TicketListing {
  const [section, product, rowLabel, qty, qtyAvail, marketNow, perks, badges, viewScore, viewLabel, type] = row;
  return {
    id,
    event_id: EVENT_ID,
    section_id: null,
    section_number: section,
    row_label: rowLabel,
    product_name: product,
    listing_type: type,
    quantity: qty,
    quantity_available: qtyAvail,
    price: frontrowlyPrice(marketNow),
    compare_at_price: frontrowlyCompareAt(marketNow),
    currency: "USD",
    perks,
    badges,
    view_score: viewScore,
    view_label: viewLabel,
    status: "available",
    sort_order: 0,
  };
}

export const mockSofiQfListings: TicketListing[] = RAW.map((row, i) =>
  toListing(\`sq-\${i}\`, row)
);

export const SOFI_QF_EVENT_ID = EVENT_ID;
`;

const sqlPath = join(__dirname, "..", "supabase", "seed", "sofi-qf-listings.sql");
const mockPath = join(__dirname, "..", "src", "lib", "data", "mock-sofi-qf-listings.ts");

writeFileSync(sqlPath, sql, "utf8");
writeFileSync(mockPath, mockTs, "utf8");
console.log(`Wrote ${SOFI_QF_LISTINGS.length} listings (min $${minPrice}) → ${sqlPath}`);
console.log(`→ ${mockPath}`);
