/**
 * Resolve World Cup 2026 knockout teams from verified R32 lineups + results.
 * Run: node scripts/resolve-wc2026-bracket.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = join(__dirname, "data");

function loadJson(name) {
  return JSON.parse(readFileSync(join(dataDir, name), "utf8"));
}

const r32Teams = loadJson("wc2026-r32-teams.json").teams;
const results = loadJson("wc2026-knockout-results.json").results;
const fixtures = loadJson("wc2026-fixtures.json");

const winners = {};
for (const [num, teams] of Object.entries(r32Teams)) {
  const result = results[num];
  if (!result) continue;
  winners[num] = result.winner === "away" ? teams.away : teams.home;
}

function resolveWinnerRef(label) {
  const m = label.match(/Winner Match (\d+)/);
  if (!m) return label;
  return winners[m[1]] ?? label;
}

let updated = 0;
for (const f of fixtures.fixtures) {
  const n = String(f.matchNumber);

  if (r32Teams[n]) {
    const { home, away } = r32Teams[n];
    if (f.homeTeam !== home || f.awayTeam !== away) {
      f.homeTeam = home;
      f.awayTeam = away;
      updated++;
    }
  }

  if (f.stage === "round-of-16") {
    const home = resolveWinnerRef(f.homeTeam);
    const away = resolveWinnerRef(f.awayTeam);
    if (home !== f.homeTeam || away !== f.awayTeam) {
      f.homeTeam = home;
      f.awayTeam = away;
      updated++;
    }
  }
}

writeFileSync(
  join(dataDir, "wc2026-fixtures.json"),
  JSON.stringify(fixtures, null, 2) + "\n"
);

console.log("R32 lineups (80–88):");
for (let i = 80; i <= 88; i++) {
  const t = r32Teams[String(i)];
  console.log(`  ${i}: ${t.home} vs ${t.away}`);
}

console.log("\nR32 winners:");
for (const [n, w] of Object.entries(winners).sort((a, b) => Number(a[0]) - Number(b[0]))) {
  console.log(`  W${n}: ${w}`);
}

console.log("\nR16 resolved:");
for (const f of fixtures.fixtures.filter((x) => x.stage === "round-of-16")) {
  const pending =
    f.homeTeam.startsWith("Winner") || f.awayTeam.startsWith("Winner");
  console.log(
    `  ${f.matchNumber}: ${f.homeTeam} vs ${f.awayTeam}${pending ? " (partial/TBD)" : ""}`
  );
}

console.log(`\nUpdated ${updated} fields in wc2026-fixtures.json`);

// Emit Supabase patch for changed catalogue matches (80+)
const patchLines = [
  "-- World Cup 2026 knockout team updates (safe to re-run)",
  "-- Generated by scripts/resolve-wc2026-bracket.mjs",
  "",
];

const slugFor = (n) => `world-cup-match-${n}`;
const teamSlug = (name) =>
  ({
    "United States": "usa",
    "Bosnia and Herzegovina": "bosnia-herzegovina",
    "Korea Republic": "south-korea",
    "Congo DR": "congo-dr",
    "Cote d'Ivoire": "cote-divoire",
    "Cabo Verde": "cabo-verde",
  })[name] ??
  name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");

function sqlStr(s) {
  return `'${String(s).replace(/'/g, "''")}'`;
}

function sqlTeamRef(name) {
  if (name.startsWith("Winner")) {
    return { id: "NULL", label: sqlStr(name) };
  }
  return { id: `(SELECT id FROM teams WHERE slug = '${teamSlug(name)}')`, label: "NULL" };
}

for (const f of fixtures.fixtures) {
  if (f.matchNumber < 80) continue;
  const home = sqlTeamRef(f.homeTeam);
  const away = sqlTeamRef(f.awayTeam);
  patchLines.push(
    `UPDATE events SET`,
    `  title = ${sqlStr(`${f.homeTeam} vs ${f.awayTeam}`)},`,
    `  home_team_id = ${home.id},`,
    `  home_team_label = ${home.label},`,
    `  away_team_id = ${away.id},`,
    `  away_team_label = ${away.label}`,
    `WHERE slug = '${slugFor(f.matchNumber)}';`,
    ""
  );
}

writeFileSync(
  join(dataDir, "world-cup-knockout-patch.sql"),
  patchLines.join("\n")
);
console.log("→ scripts/data/world-cup-knockout-patch.sql");

